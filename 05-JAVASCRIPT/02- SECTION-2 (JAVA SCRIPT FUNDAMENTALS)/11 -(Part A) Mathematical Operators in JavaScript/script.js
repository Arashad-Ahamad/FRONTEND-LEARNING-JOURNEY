// Mathematical Operators

const a = 10; 
const b = 3;

// Addition
console.log(a + b); // Output: 13

// Subtraction
console.log(a - b); // Output: 7

// Multiplication
console.log(a * b); // Output: 30

// Division
console.log(a / b); // Output: 3.3333333333333335

// Remainder (Modulus)
console.log(a % b); // Output: 1

const m = 20;
const n = 5;

console.log(m % n); // Output: 0

// Exponentiation Operator
const g = 5;
console.log(g ** 2); // Output: 25 (5 raised to the power of 2)
console.log(g ** 3); // Output: 125 (5 raised to the power of 3)
console.log(g ** 4); // Output: 625 (5 raised to the power of 4)

const x = 2;
const y = 4;
console.log(x ** y); // Output: 16 (2 raised to the power of 4)

