//---------------------------------------------------------------
// Do while Loop in JavaScript
//---------------------------------------------------------------
// Example 1:
let i = 1;

do {
  console.log(i);  // First, it will print the value of i
  i++;             // Then, it will increase the value of i by 1
} while (i <= 5);   // Then, it will check: is i less than or equal to 5?

//---------------------------------------------------------------
// Example 2:
let count = 1
do {
    console.log('Hello');
    count++
} while(count <= 1)

